@dataclass(frozen=True, slots=True)
class PartitionLedgerCertificate:
    """Checksums for the finite combinatorial candidate ledger."""

    atom_kind_counts: tuple[tuple[FiberKind, int], ...]
    atom_names_by_kind: tuple[tuple[FiberKind, tuple[str, ...]], ...]
    profile_count: int
    codimension_histogram: tuple[int, ...]
    codimension_one_names: tuple[str, ...]
    codimension_two_names: tuple[str, ...]
    endpoint_count: int
    overdetermined_count: int
    coefficient_slice_minors: tuple[Expr, Expr]
    coefficient_slice_minor_gcd: Expr

    @property
    def verified(self) -> bool:
        """Whether all exact combinatorial and coefficient checks agree."""

        return bool(
            self.atom_kind_counts == EXPECTED_ATOM_KIND_COUNTS
            and self.atom_names_by_kind == EXPECTED_ATOM_NAMES_BY_KIND
            and sum(count for _, count in self.atom_kind_counts) == 35
            and self.profile_count == 145
            and self.codimension_histogram == EXPECTED_CODIMENSION_HISTOGRAM
            and self.codimension_one_names == EXPECTED_CODIMENSION_ONE_NAMES
            and self.codimension_two_names == EXPECTED_CODIMENSION_TWO_NAMES
            and self.endpoint_count == 38
            and self.overdetermined_count == 55
            and self.coefficient_slice_minors[0] == 256 * KAPPA
            and (self.coefficient_slice_minors[1] - 128 * (3 * KAPPA**2 + 4)).expand()
            == 0
            and self.coefficient_slice_minor_gcd == 1
        )


@cache
def exact_partition_ledger_certificate() -> PartitionLedgerCertificate:
    """Build the exact delta-ten candidate-ledger certificate."""

    atoms = local_atoms()
    profiles = global_profiles()
    kind_counts = Counter(atom.kind for atom in atoms)
    first_minor, second_minor, minor_gcd = coefficient_slice_rank_certificate()
    return PartitionLedgerCertificate(
        atom_kind_counts=tuple((kind, kind_counts.get(kind, 0)) for kind in FiberKind),
        atom_names_by_kind=tuple(
            (
                kind,
                tuple(sorted(atom.name for atom in atoms if atom.kind == kind)),
            )
            for kind in FiberKind
        ),
        profile_count=len(profiles),
        codimension_histogram=expected_codimension_histogram(),
        codimension_one_names=tuple(
            profile.name for profile in profiles_with_expected_codimension(1)
        ),
        codimension_two_names=tuple(
            profile.name for profile in profiles_with_expected_codimension(2)
        ),
        endpoint_count=len(profiles_with_expected_codimension(5)),
        overdetermined_count=sum(
            profile.expected_codimension > PARAMETER_SPACE_DIMENSION
            for profile in profiles
        ),
        coefficient_slice_minors=(first_minor, second_minor),
        coefficient_slice_minor_gcd=minor_gcd,
    )


def main() -> int:
    """Print the candidate ledger and its explicit claim boundary."""

    certificate = exact_partition_ledger_certificate()
    print("local atom counts:", certificate.atom_kind_counts)
    print("global profiles:", certificate.profile_count)
    print("expected-codimension histogram:", certificate.codimension_histogram)
    print("expected divisors:", certificate.codimension_one_names)
    print("expected codimension-two profiles:", certificate.codimension_two_names)
    print(
        "expected endpoints:",
        tuple(profile.name for profile in profiles_with_expected_codimension(5)),
    )
    print(
        "overdetermined candidates needing elimination:",
        certificate.overdetermined_count,
    )
    print("coefficient-slice rank minors:", certificate.coefficient_slice_minors)
    print("delta-ten partition ledger verified:", certificate.verified)
    print(
        "claim boundary: codimensions are combinatorial expectations; "
        "realization and exclusion require saturated elimination"
    )
    return 0 if certificate.verified else 1


