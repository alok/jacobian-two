@dataclass(frozen=True)
class OneDicriticalPassport:
    """One exact group/class/ramification passport under one dicritical."""

    group_identifier: str
    group_structure: str
    automorphism_order: int
    ramification_index: int
    tangential_degree: int
    jump_defect: int
    inertia_class: ConjugacyClassAnalysis

    @property
    def forced_tangential_jump(self) -> int:
        """Return the part of ``jump_defect`` forced by tangential degree."""

        return self.ramification_index * (self.tangential_degree - 1)

    @property
    def residual_excess(self) -> int:
        """Return the jump budget left after the Riemann--Hurwitz minimum."""

        return self.jump_defect - self.forced_tangential_jump

    @property
    def survives_deck_rigidity(self) -> bool:
        """Whether triviality of polynomial deck symmetries keeps the passport."""

        return self.automorphism_order == 1

    @property
    def survives_contractible_branch_obstruction(self) -> bool:
        """Whether the final one-dicritical local/global filter keeps it.

        The geometric proof, external to this finite certificate, eliminates
        ``(e,d)=(2,2)`` and ``(4,1)``: their forced branch curve is
        contractible, so Lin--Zaidenberg identifies its global complement with
        the local cusp complement, contradicting the intransitive local fiber.
        """

        return (
            self.survives_deck_rigidity
            and (self.ramification_index, self.tangential_degree)
            not in {(2, 2), (4, 1)}
        )


@dataclass(frozen=True)
class OneDicriticalS6FiberProfile:
    """One target-fiber row for the surviving ``S6, (e,d)=(2,1)`` case.

    ``jumps`` contains ``kappa(t)=mu_t-2`` for the distinct normalization
    points above one target value.  It is sorted only to make the exhaustive
    table canonical; no ordering of geometric branches is intended.
    """

    jumps: tuple[int, ...]

    @property
    def boundary_block_sizes(self) -> tuple[int, ...]:
        """Return the local boundary degrees ``2 + kappa(t)``."""

        return tuple(2 + jump for jump in self.jumps)

    @property
    def affine_fiber_size(self) -> int:
        """Return the exact number of affine preimages at the target value."""

        return DEGREE - sum(self.boundary_block_sizes)

    @property
    def is_omitted(self) -> bool:
        """Whether the original affine Keller map omits the target value."""

        return self.affine_fiber_size == 0


