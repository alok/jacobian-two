def compose(left: Permutation, right: Permutation) -> Permutation:
    """Return ``left ∘ right``."""

    return tuple(left[right[point]] for point in range(DEGREE))


def inverse(permutation: Permutation) -> Permutation:
    """Return the inverse of a permutation."""

    result = [0] * DEGREE
    for source, target in enumerate(permutation):
        result[target] = source
    return tuple(result)


def generated_group(generators: Iterable[Permutation]) -> PermutationGroup:
    """Enumerate the finite subgroup generated by ``generators`` exactly."""

    generator_tuple = tuple(generators)
    if not generator_tuple:
        return frozenset({IDENTITY})
    seen = {IDENTITY}
    frontier = [IDENTITY]
    while frontier:
        element = frontier.pop()
        for generator in generator_tuple:
            candidate = compose(element, generator)
            if candidate not in seen:
                seen.add(candidate)
                frontier.append(candidate)
    return frozenset(seen)


