"""Exact finite-group certificate for the six-sheet Jacobian frontier.

For a separable extension ``L/K`` of degree six, let ``M`` be its normal
closure, ``G = Gal(M/K)``, and ``H = Gal(M/L)``.  The faithful action of
``G`` on ``G/H`` is one of GAP's sixteen transitive groups of degree six.

This module stores the generators returned by GAP 4.14.0 for ``6T1`` through
``6T16`` and independently recomputes the invariants used in the research
argument with a small, dependency-free permutation engine.  It deliberately
does *not* assert that every abstract candidate is realizable by a Keller
map; the certificate only identifies what finite group theory can exclude.
"""

from __future__ import annotations

from collections.abc import Iterable, Sequence
from dataclasses import dataclass
from itertools import combinations, combinations_with_replacement, product
from typing import Final, TypeAlias

Permutation: TypeAlias = tuple[int, ...]
PermutationGroup: TypeAlias = frozenset[Permutation]
Block: TypeAlias = frozenset[int]
CycleType: TypeAlias = tuple[int, ...]

DEGREE: Final = 6
IDENTITY: Final[Permutation] = tuple(range(DEGREE))
OREVKOV_SIX_SHEET_BUDGET: Final = DEGREE - 1
OREVKOV_FORBIDDEN_RAMIFICATION_INDEX: Final = DEGREE - 1
OREVKOV_ONE_DICRITICAL_TYPES: Final[frozenset[CycleType]] = frozenset(
    {
        (2, 1, 1, 1, 1),
        (2, 2, 1, 1),
        (3, 1, 1, 1),
        (4, 1, 1),
    }
)


def _from_cycles(*cycles: tuple[int, ...]) -> Permutation:
    """Build a permutation from disjoint cycles written with GAP's 1-based points."""

    image = list(IDENTITY)
    support: set[int] = set()
    for cycle in cycles:
        if len(cycle) < 2:
            msg = "cycles must contain at least two points"
            raise ValueError(msg)
        zero_based = tuple(point - 1 for point in cycle)
        if any(point < 0 or point >= DEGREE for point in zero_based):
            msg = f"cycle point outside 1..{DEGREE}"
            raise ValueError(msg)
        if support.intersection(zero_based):
            msg = "cycles must be disjoint"
            raise ValueError(msg)
        support.update(zero_based)
        for source, target in zip(zero_based, zero_based[1:] + zero_based[:1]):
            image[source] = target
    return tuple(image)


