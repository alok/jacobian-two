def point_stabilizer(group: PermutationGroup, point: int = 0) -> PermutationGroup:
    """Return the stabilizer of ``point``."""

    return frozenset(element for element in group if element[point] == point)


def conjugate(element: Permutation, subgroup_element: Permutation) -> Permutation:
    """Return ``element * subgroup_element * element^-1``."""

    return compose(compose(element, subgroup_element), inverse(element))


def normalizer(group: PermutationGroup, subgroup: PermutationGroup) -> PermutationGroup:
    """Return the normalizer of ``subgroup`` inside ``group``."""

    return frozenset(
        element
        for element in group
        if frozenset(conjugate(element, member) for member in subgroup) == subgroup
    )


def is_transitive(group: PermutationGroup) -> bool:
    """Whether the orbit of the first point is all six points."""

    return {element[0] for element in group} == set(range(DEGREE))


def has_fixed_point(permutation: Permutation) -> bool:
    """Whether ``permutation`` fixes at least one of the six sheets."""

    return any(permutation[point] == point for point in range(DEGREE))


def cycle_type(permutation: Permutation) -> CycleType:
    """Return the complete cycle partition, including fixed points."""

    visited: set[int] = set()
    lengths: list[int] = []
    for starting_point in range(DEGREE):
        if starting_point in visited:
            continue
        point = starting_point
        length = 0
        while point not in visited:
            visited.add(point)
            point = permutation[point]
            length += 1
        lengths.append(length)
    return tuple(sorted(lengths, reverse=True))


@dataclass(frozen=True)
class ConjugacyClassAnalysis:
    """Cycle data and exact normal closure of one conjugacy class."""

    representative: Permutation
    cycle_type: CycleType
    class_size: int
    normal_closure_order: int

    @property
    def moved_sheet_count(self) -> int:
        """Return the number of sheets moved by a class representative.

        For a generic branch meridian this is also the minimum contribution
        ``sum(e*d)`` forced by the tangentially refined Orevkov identity.
        """

        return sum(length for length in self.cycle_type if length > 1)

    @property
    def avoids_forbidden_index(self) -> bool:
        """Whether no cycle requires Orevkov's forbidden index ``N - 1``."""

        return OREVKOV_FORBIDDEN_RAMIFICATION_INDEX not in self.cycle_type


def conjugacy_class_analyses(group: PermutationGroup) -> tuple[ConjugacyClassAnalysis, ...]:
    """Enumerate classes separately even when they have the same cycle type."""

    remaining = set(group)
    result: list[ConjugacyClassAnalysis] = []
    while remaining:
        representative = min(remaining)
        conjugacy_class = frozenset(
            conjugate(element, representative) for element in group
        )
        result.append(
            ConjugacyClassAnalysis(
                representative=representative,
                cycle_type=cycle_type(representative),
                class_size=len(conjugacy_class),
                normal_closure_order=len(generated_group(conjugacy_class)),
            )
        )
        remaining.difference_update(conjugacy_class)
    return tuple(
        sorted(
            result,
            key=lambda analysis: (
                analysis.cycle_type,
                analysis.class_size,
                analysis.normal_closure_order,
                analysis.representative,
            ),
        )
    )


def conjugacy_class(
    group: PermutationGroup,
    representative: Permutation,
) -> PermutationGroup:
    """Return the conjugacy class of ``representative`` inside ``group``."""

    if representative not in group:
        msg = "representative must belong to the group"
        raise ValueError(msg)
    return frozenset(conjugate(element, representative) for element in group)


def _integer_partitions(
    total: int,
    minimum_part: int = 1,
) -> tuple[tuple[int, ...], ...]:
    """Return nondecreasing positive partitions of ``total`` exactly once."""

    if total < 0:
        msg = "partition total must be nonnegative"
        raise ValueError(msg)
    if total == 0:
        return ((),)
    result: list[tuple[int, ...]] = []
    for first in range(minimum_part, total + 1):
        for tail in _integer_partitions(total - first, first):
            result.append((first, *tail))
    return tuple(result)


@dataclass(frozen=True)
class BranchInertiaRealization:
    """Dicritical components realizing one generic branch meridian.

    Each pair is ``(e, d)``: normal ramification index ``e`` and tangential
    degree ``d``.  One component contributes ``d`` disjoint ``e``-cycles to
    the meridian.  Orevkov's outer term starts at ``e``, while
    Riemann--Hurwitz forces at least ``e*(d-1)`` finite jump units.  Its true
    minimum contribution to the exact defect budget is therefore ``e*d``.
    """

    components: tuple[tuple[int, int], ...]

    @property
    def outer_sum_cost(self) -> int:
        """Return the unrefined ``sum(e)`` contribution."""

        return sum(index for index, _degree in self.components)

    @property
    def forced_tangential_jump(self) -> int:
        """Return the jump defect forced by finite tangential ramification."""

        return sum(
            index * (tangential_degree - 1)
            for index, tangential_degree in self.components
        )

    @property
    def ramification_cost(self) -> int:
        """Return the refined minimum ``sum(e*d)`` defect contribution."""

        return self.outer_sum_cost + self.forced_tangential_jump


def branch_inertia_realizations(
    partition: CycleType,
) -> tuple[BranchInertiaRealization, ...]:
    """Enumerate all component decompositions of a generic inertia cycle type.

    Fixed points do not require ramified dicritical components.  Repeated
    cycles of the same length may come either from one component of higher
    tangential degree or from several components over the same branch curve.
    """

    if sum(partition) != DEGREE or any(length < 1 for length in partition):
        msg = f"cycle type must be a partition of {DEGREE}"
        raise ValueError(msg)
    multiplicities: dict[int, int] = {}
    for length in partition:
        if length > 1:
            multiplicities[length] = multiplicities.get(length, 0) + 1
    if not multiplicities:
        return (BranchInertiaRealization(components=()),)

    choices_by_index: list[tuple[tuple[tuple[int, int], ...], ...]] = []
    for index, multiplicity in sorted(multiplicities.items()):
        choices_by_index.append(
            tuple(
                tuple((index, tangential_degree) for tangential_degree in degrees)
                for degrees in _integer_partitions(multiplicity)
            )
        )

    result = tuple(
        BranchInertiaRealization(
            components=tuple(
                component
                for same_index_components in choices
                for component in same_index_components
            )
        )
        for choices in product(*choices_by_index)
    )
    return tuple(
        sorted(
            result,
            key=lambda realization: (
                realization.ramification_cost,
                len(realization.components),
                realization.components,
            ),
        )
    )


@dataclass(frozen=True)
class InertiaClassPairAnalysis:
    """Joint normal-closure and minimum Orevkov cost of two branch classes."""

    first: ConjugacyClassAnalysis
    second: ConjugacyClassAnalysis
    joint_normal_closure_order: int

    @property
    def minimum_realizations(
        self,
    ) -> tuple[BranchInertiaRealization, BranchInertiaRealization]:
        """Return minimum-cost component realizations of the two classes."""

        return (
            branch_inertia_realizations(self.first.cycle_type)[0],
            branch_inertia_realizations(self.second.cycle_type)[0],
        )

    @property
    def minimum_ramification_cost(self) -> int:
        """Return the least refined defect cost before deck-forced components."""

        return sum(
            realization.ramification_cost
            for realization in self.minimum_realizations
        )


@dataclass(frozen=True)
class RamifiedBranchProfile:
    """Normally generating branch classes within the refined degree-six budget.

    Each entry of ``branch_classes`` represents a distinct irreducible target
    branch curve.  Dicritical components over the same target curve have
    already been combined into that meridian's disjoint cycles.
    """

    group_identifier: str
    branch_classes: tuple[ConjugacyClassAnalysis, ...]
    minimum_defect_cost: int

    @property
    def residual_defect(self) -> int:
        """Return the budget left for unramified dicriticals and excess jumps."""

        return OREVKOV_SIX_SHEET_BUDGET - self.minimum_defect_cost

    @property
    def survives_irreducible_branch_obstruction(self) -> bool:
        """Whether the sole-branch normalization obstruction leaves the profile.

        If there is just one ramified branch curve and its generic inertia moves
        more than half of the six sheets, a normalization collision would cost
        more than six sheets.  Its normalization is therefore injective.  The
        Lin--Zaidenberg local/global argument rules out that configuration.
        """

        return not (
            len(self.branch_classes) == 1
            and self.branch_classes[0].moved_sheet_count * 2 > DEGREE
        )


def ramified_branch_profiles(
    fixture: TransitiveGroupFixture,
) -> tuple[RamifiedBranchProfile, ...]:
    """Enumerate every normally generating ramified profile of cost at most five.

    Riemann--Hurwitz turns a generic meridian's moved-sheet count into its
    minimum Orevkov cost.  A fixed-point class containing a five-cycle is
    omitted because it requires a dicritical of index ``N - 1``, excluded by
    Orevkov's final remark.
    """

    group = generated_group(fixture.generators)
    candidates = tuple(
        analysis
        for analysis in conjugacy_class_analyses(group)
        if analysis.representative != IDENTITY
        and has_fixed_point(analysis.representative)
        and analysis.avoids_forbidden_index
    )
    result: list[RamifiedBranchProfile] = []
    # Every nontrivial inertia moves at least two sheets, so budget five permits
    # at most two distinct ramified target curves.
    for number_of_branches in (1, 2):
        for branch_classes in combinations_with_replacement(
            candidates,
            number_of_branches,
        ):
            minimum_defect_cost = sum(
                analysis.moved_sheet_count for analysis in branch_classes
            )
            if minimum_defect_cost > OREVKOV_SIX_SHEET_BUDGET:
                continue
            generated = generated_group(
                representative
                for analysis in branch_classes
                for representative in conjugacy_class(
                    group,
                    analysis.representative,
                )
            )
            if len(generated) != len(group):
                continue
            result.append(
                RamifiedBranchProfile(
                    group_identifier=fixture.identifier,
                    branch_classes=branch_classes,
                    minimum_defect_cost=minimum_defect_cost,
                )
            )
    return tuple(
        sorted(
            result,
            key=lambda profile: (
                profile.minimum_defect_cost,
                len(profile.branch_classes),
                tuple(
                    (
                        analysis.cycle_type,
                        analysis.class_size,
                        analysis.normal_closure_order,
                    )
                    for analysis in profile.branch_classes
                ),
            ),
        )
    )


def full_generating_inertia_pairs(
    fixture: TransitiveGroupFixture,
) -> tuple[InertiaClassPairAnalysis, ...]:
    """Return fixed-sheet class pairs whose joint normal closure is ``G``.

    Classes are not deduplicated by ambient cycle type.  This is essential for
    ``6T11``, which has two double-transposition classes with different normal
    closures.
    """

    group = generated_group(fixture.generators)
    allowed_classes = tuple(
        analysis
        for analysis in conjugacy_class_analyses(group)
        if analysis.cycle_type in OREVKOV_ONE_DICRITICAL_TYPES
    )
    result: list[InertiaClassPairAnalysis] = []
    for first, second in combinations_with_replacement(allowed_classes, 2):
        generated = generated_group(
            (
                *conjugacy_class(group, first.representative),
                *conjugacy_class(group, second.representative),
            )
        )
        if len(generated) == len(group):
            result.append(
                InertiaClassPairAnalysis(
                    first=first,
                    second=second,
                    joint_normal_closure_order=len(generated),
                )
            )
    return tuple(result)


