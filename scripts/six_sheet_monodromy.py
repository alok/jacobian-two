def one_dicritical_s6_fiber_profiles() -> tuple[OneDicriticalS6FiberProfile, ...]:
    """Enumerate the complete finite fiber census for the ``S6`` passport.

    Orevkov gives total jump budget three.  Constant multiplicity six says
    that a target with normalization-branch jumps ``kappa_i`` has exactly
    ``6 - sum(2 + kappa_i)`` affine preimages.
    """

    result: list[OneDicriticalS6FiberProfile] = []
    for number_of_branches in range(1, DEGREE // 2 + 1):
        for jumps in combinations_with_replacement(
            range(OREVKOV_SIX_SHEET_BUDGET - 1),
            number_of_branches,
        ):
            if sum(jumps) > OREVKOV_SIX_SHEET_BUDGET - 2:
                continue
            profile = OneDicriticalS6FiberProfile(jumps=jumps)
            if profile.affine_fiber_size >= 0:
                result.append(profile)
    return tuple(
        sorted(
            result,
            key=lambda profile: (
                len(profile.jumps),
                sum(profile.jumps),
                profile.jumps,
            ),
        )
    )


@dataclass(frozen=True)
class LocalMonodromyOvergroup:
    """An overgroup normally generated by a chosen local meridian."""

    subgroup: PermutationGroup
    orbit_sizes: tuple[int, ...]

    @property
    def order(self) -> int:
        """Return the subgroup order."""

        return len(self.subgroup)


def permutation_orbit_sizes(group: PermutationGroup) -> tuple[int, ...]:
    """Return the sizes of all sheet orbits in decreasing order."""

    remaining = set(range(DEGREE))
    result: list[int] = []
    while remaining:
        point = min(remaining)
        orbit = {element[point] for element in group}
        result.append(len(orbit))
        remaining.difference_update(orbit)
    return tuple(sorted(result, reverse=True))


def normally_generated_overgroups(
    group: PermutationGroup,
    meridian: Permutation,
) -> tuple[LocalMonodromyOvergroup, ...]:
    """Enumerate overgroups normally generated by one meridian.

    A local monodromy group is such an overgroup when the *entire* local
    discriminant is one irreducible branch germ: the complement's fundamental
    group is then normally generated by a meridian.  Its sheet orbits are the
    connected local components, whose sizes are their local multiplicities.
    Merely knowing that one discriminant component is irreducible is not enough
    if another local branch is present.
    """

    if meridian not in group:
        msg = "meridian must belong to the ambient group"
        raise ValueError(msg)
    initial = generated_group((meridian,))
    overgroups = {initial}
    frontier = [initial]
    while frontier:
        subgroup = frontier.pop()
        for element in group.difference(subgroup):
            larger = generated_group((*subgroup, element))
            if larger not in overgroups:
                overgroups.add(larger)
                frontier.append(larger)

    result = tuple(
        LocalMonodromyOvergroup(
            subgroup=subgroup,
            orbit_sizes=permutation_orbit_sizes(subgroup),
        )
        for subgroup in overgroups
        if generated_group(conjugacy_class(subgroup, meridian)) == subgroup
    )
    return tuple(
        sorted(
            result,
            key=lambda analysis: (
                analysis.order,
                analysis.orbit_sizes,
                tuple(sorted(analysis.subgroup)),
            ),
        )
    )


def _contains_indices(indices: Sequence[int], required: Sequence[int]) -> bool:
    """Whether the first multiset contains the second multiset."""

    return all(
        indices.count(index) >= required.count(index)
        for index in set(required)
    )


def orevkov_budget_profiles(
    *,
    required_indices: Sequence[int] = (),
    minimum_unramified_components: int = 0,
) -> tuple[OrevkovBudgetProfile, ...]:
    """Enumerate exact degree-six profiles under Orevkov's defect identity.

    The value five is ``N-1``.  Index five is omitted because Orevkov's final
    remark excludes a dicritical component of index ``N-1`` for a Keller map.
    """

    if minimum_unramified_components < 0:
        msg = "minimum number of unramified components must be nonnegative"
        raise ValueError(msg)
    if any(
        index < 1 or index >= OREVKOV_SIX_SHEET_BUDGET
        for index in required_indices
    ):
        msg = "required indices must lie between one and four"
        raise ValueError(msg)

    result: list[OrevkovBudgetProfile] = []
    for component_count in range(1, OREVKOV_SIX_SHEET_BUDGET + 1):
        for indices in combinations_with_replacement(
            range(1, OREVKOV_SIX_SHEET_BUDGET),
            component_count,
        ):
            index_sum = sum(indices)
            if index_sum > OREVKOV_SIX_SHEET_BUDGET:
                continue
            if indices.count(1) < minimum_unramified_components:
                continue
            if not _contains_indices(indices, required_indices):
                continue
            result.append(
                OrevkovBudgetProfile(
                    dicritical_indices=indices,
                    jump_defect=OREVKOV_SIX_SHEET_BUDGET - index_sum,
                )
            )
    return tuple(result)


def fixed_point_normal_closure(group: PermutationGroup) -> PermutationGroup:
    """Return the normal subgroup generated by all fixed-point elements.

    Fixed-point elements form a union of conjugacy classes, so the subgroup
    they generate is already normal.  For a hypothetical Keller map this
    subgroup must be all of ``G``: branch meridians normally generate the
    monodromy, while every branch inertia element fixes an affine local sheet.
    The latter geometric premise is proved outside this finite calculation.
    """

    return generated_group(
        element
        for element in group
        if element != IDENTITY and has_fixed_point(element)
    )


def blocks_containing_first_point(group: PermutationGroup) -> tuple[Block, ...]:
    """Enumerate all proper non-singleton blocks containing point zero."""

    blocks: list[Block] = []
    other_points = tuple(range(1, DEGREE))
    for size in range(2, DEGREE):
        for tail in combinations(other_points, size - 1):
            block = frozenset((0, *tail))
            is_block = True
            for element in group:
                image = frozenset(element[point] for point in block)
                if image != block and not image.isdisjoint(block):
                    is_block = False
                    break
            if is_block:
                blocks.append(block)
    return tuple(blocks)


def _cosets(group: PermutationGroup, subgroup: PermutationGroup) -> tuple[PermutationGroup, ...]:
    """Return left cosets of ``subgroup`` in ``group`` in deterministic order."""

    remaining = set(group)
    result: list[PermutationGroup] = []
    while remaining:
        representative = min(remaining)
        coset = frozenset(compose(representative, member) for member in subgroup)
        result.append(coset)
        remaining.difference_update(coset)
    return tuple(result)


def quotient_structure(normalizer_group: PermutationGroup, subgroup: PermutationGroup) -> str:
    """Identify the small quotient ``normalizer_group / subgroup`` exactly."""

    cosets = _cosets(normalizer_group, subgroup)
    coset_of = {element: index for index, coset in enumerate(cosets) for element in coset}
    representatives = tuple(min(coset) for coset in cosets)
    multiplication = tuple(
        tuple(coset_of[compose(left, right)] for right in representatives)
        for left in representatives
    )
    order = len(cosets)
    if order == 1:
        return "1"
    if order in {2, 3}:
        return f"C{order}"
    if order == 6:
        abelian = all(
            multiplication[left][right] == multiplication[right][left]
            for left in range(order)
            for right in range(order)
        )
        return "C6" if abelian else "S3"
    msg = f"unexpected automorphism-group order {order}"
    raise ValueError(msg)


@dataclass(frozen=True)
class SixSheetGroupAnalysis:
    """Exact Galois-theoretic invariants of one transitive degree-six group."""

    identifier: str
    structure: str
    order: int
    stabilizer_order: int
    normalizer_order: int
    automorphism_group: str
    automorphism_order: int
    is_galois: bool
    is_primitive: bool
    block_sizes: tuple[int, ...]
    fixed_point_normal_closure_order: int
    one_dicritical_witnesses: tuple[ConjugacyClassAnalysis, ...]

    @property
    def factor_degrees(self) -> tuple[tuple[int, int], ...]:
        """Return possible ``(inner, outer)`` rational factor degrees."""

        return tuple((size, DEGREE // size) for size in self.block_sizes)

    @property
    def affine_inertia_compatible(self) -> bool:
        """Whether fixed-point inertia elements can normally generate ``G``."""

        return self.fixed_point_normal_closure_order == self.order

    @property
    def general_keller_compatible(self) -> bool:
        """Whether the proved unrestricted degree-six filters leave ``G``.

        The fixed-sheet normal-generation theorem first leaves seven groups.
        The deck/budget/local-monodromy arguments eliminate its three
        imprimitive entries.  The refined ``sum(e*d + delta) = 5`` identity
        and the irreducible-branch normalization obstruction then eliminate
        ``6T12=A5`` and ``6T14=S5``.  Those geometric arguments are proved
        outside this finite certificate.
        """

        return self.identifier in {"6T15", "6T16"}

    @property
    def one_dicritical_inertia_compatible(self) -> bool:
        """Whether one Orevkov-allowed inertia class normally generates ``G``.

        This is conditional on the one-dicritical hypothesis.  That hypothesis
        implies an irreducible nonproper curve, but the converse is not used or
        claimed: several dicritical boundary components may dominate the same
        irreducible curve.  The filter is not asserted for an arbitrary Keller
        map with several dicritical components.
        """

        return bool(self.one_dicritical_witnesses)

    @property
    def one_dicritical_keller_compatible(self) -> bool:
        """Whether the final one-dicritical filters leave a passport for ``G``.

        In the one-dicritical Keller setting, the unique boundary divisor is
        ramified and deck-invariant; purity of the affine-open boundary then
        makes every field deck transformation polynomial.  Polynomial deck
        transformations of a Keller map are trivial.  The subsequent
        Lin--Zaidenberg local/global obstruction also removes the forced
        ``(e,d)=(2,2)`` and ``(4,1)`` passports.
        """

        return any(
            passport.survives_contractible_branch_obstruction
            for passport in passports_for_analysis(self)
        )

    @property
    def one_dicritical_deck_compatible(self) -> bool:
        """Whether inertia and the intermediate deck-rigidity filter allow ``G``."""

        return (
            self.one_dicritical_inertia_compatible
            and self.automorphism_order == 1
        )


def analyze_fixture(fixture: TransitiveGroupFixture) -> SixSheetGroupAnalysis:
    """Compute all table invariants from generators, without GAP at runtime."""

    group = generated_group(fixture.generators)
    if not is_transitive(group):
        msg = f"{fixture.identifier} fixture is not transitive"
        raise ValueError(msg)
    stabilizer = point_stabilizer(group)
    normalizer_group = normalizer(group, stabilizer)
    blocks = blocks_containing_first_point(group)
    block_sizes = tuple(sorted({len(block) for block in blocks}))
    class_analyses = conjugacy_class_analyses(group)
    one_dicritical_witnesses = tuple(
        analysis
        for analysis in class_analyses
        if analysis.cycle_type in OREVKOV_ONE_DICRITICAL_TYPES
        and analysis.normal_closure_order == len(group)
    )
    return SixSheetGroupAnalysis(
        identifier=fixture.identifier,
        structure=fixture.structure,
        order=len(group),
        stabilizer_order=len(stabilizer),
        normalizer_order=len(normalizer_group),
        automorphism_group=quotient_structure(normalizer_group, stabilizer),
        automorphism_order=len(normalizer_group) // len(stabilizer),
        is_galois=normalizer_group == group,
        is_primitive=not blocks,
        block_sizes=block_sizes,
        fixed_point_normal_closure_order=len(fixed_point_normal_closure(group)),
        one_dicritical_witnesses=one_dicritical_witnesses,
    )


def classify_six_sheet_groups() -> tuple[SixSheetGroupAnalysis, ...]:
    """Analyze all sixteen GAP transitive groups of degree six."""

    return tuple(analyze_fixture(fixture) for fixture in TRANSITIVE_GROUPS)


def passports_for_analysis(
    analysis: SixSheetGroupAnalysis,
) -> tuple[OneDicriticalPassport, ...]:
    """Convert one row's normally generating inertia classes to passports."""

    result: list[OneDicriticalPassport] = []
    for inertia_class in analysis.one_dicritical_witnesses:
        nontrivial_cycles = tuple(
            length for length in inertia_class.cycle_type if length > 1
        )
        if not nontrivial_cycles or len(set(nontrivial_cycles)) != 1:
            msg = "one-dicritical inertia must consist of equal nontrivial cycles"
            raise ValueError(msg)
        ramification_index = nontrivial_cycles[0]
        result.append(
            OneDicriticalPassport(
                group_identifier=analysis.identifier,
                group_structure=analysis.structure,
                automorphism_order=analysis.automorphism_order,
                ramification_index=ramification_index,
                tangential_degree=len(nontrivial_cycles),
                jump_defect=OREVKOV_SIX_SHEET_BUDGET - ramification_index,
                inertia_class=inertia_class,
            )
        )
    return tuple(result)


def one_dicritical_passports() -> tuple[OneDicriticalPassport, ...]:
    """Return every exact passport surviving the raw one-class inertia test."""

    return tuple(
        passport
        for analysis in classify_six_sheet_groups()
        for passport in passports_for_analysis(analysis)
    )


def candidate_ids(
    *,
    non_galois: bool = True,
    primitive: bool | None = None,
    trivial_automorphisms: bool | None = None,
    affine_inertia_compatible: bool | None = None,
    general_keller_compatible: bool | None = None,
    one_dicritical_inertia_compatible: bool | None = None,
    one_dicritical_deck_compatible: bool | None = None,
    one_dicritical_keller_compatible: bool | None = None,
) -> tuple[str, ...]:
    """Filter the exact table by hypotheses, without claiming Keller realizability."""

    result: list[str] = []
    for analysis in classify_six_sheet_groups():
        if non_galois and analysis.is_galois:
            continue
        if primitive is not None and analysis.is_primitive != primitive:
            continue
        has_trivial_automorphisms = analysis.automorphism_order == 1
        if (
            trivial_automorphisms is not None
            and has_trivial_automorphisms != trivial_automorphisms
        ):
            continue
        if (
            affine_inertia_compatible is not None
            and analysis.affine_inertia_compatible != affine_inertia_compatible
        ):
            continue
        if (
            general_keller_compatible is not None
            and analysis.general_keller_compatible
            != general_keller_compatible
        ):
            continue
        if (
            one_dicritical_inertia_compatible is not None
            and analysis.one_dicritical_inertia_compatible
            != one_dicritical_inertia_compatible
        ):
            continue
        if (
            one_dicritical_deck_compatible is not None
            and analysis.one_dicritical_deck_compatible
            != one_dicritical_deck_compatible
        ):
            continue
        if (
            one_dicritical_keller_compatible is not None
            and analysis.one_dicritical_keller_compatible
            != one_dicritical_keller_compatible
        ):
            continue
        result.append(analysis.identifier)
    return tuple(result)


def _format_bool(value: bool) -> str:
    return "yes" if value else "no"


def render_table(rows: Sequence[SixSheetGroupAnalysis]) -> str:
    """Render a compact, deterministic Markdown table."""

    header = (
        "| id | G | ord(G) | ord(H) | Aut_K(L) | Galois | primitive | blocks | inertia |\n"
        "|---|---|---:|---:|---|---|---|---|---|"
    )
    body = []
    for row in rows:
        blocks = ",".join(str(size) for size in row.block_sizes) or "-"
        body.append(
            f"| {row.identifier} | {row.structure} | {row.order} | "
            f"{row.stabilizer_order} | {row.automorphism_group} | "
            f"{_format_bool(row.is_galois)} | {_format_bool(row.is_primitive)} | "
            f"{blocks} | {_format_bool(row.affine_inertia_compatible)} |"
        )
    return "\n".join((header, *body))


